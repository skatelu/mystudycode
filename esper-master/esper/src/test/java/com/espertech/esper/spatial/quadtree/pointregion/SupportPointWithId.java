package com.espertech.esper.spatial.quadtree.pointregion;

public class SupportPointWithId {
    private final String id;
    private double x;
    private double y;

    public SupportPointWithId(String id, double x, double y) {
        this.id = id;
        this.x = x;
        this.y = y;
    }

    public String getId() {
        return id;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public void setX(double x) {
        this.x = x;
    }

    public void setY(double y) {
        this.y = y;
    }
}
