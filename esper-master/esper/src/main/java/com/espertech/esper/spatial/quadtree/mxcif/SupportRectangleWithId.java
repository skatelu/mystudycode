package com.espertech.esper.spatial.quadtree.mxcif;

public class SupportRectangleWithId {
    private final String id;
    private double x;
    private double y;
    private double w;
    private double h;

    public SupportRectangleWithId(String id, double x, double y, double w, double h) {
        this.id = id;
        this.x = x;
        this.y = y;
        this.w = w;
        this.h = h;
    }

    public String getId() {
        return id;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    public double getW() {
        return w;
    }

    public void setW(double w) {
        this.w = w;
    }

    public double getH() {
        return h;
    }

    public void setH(double h) {
        this.h = h;
    }
}
