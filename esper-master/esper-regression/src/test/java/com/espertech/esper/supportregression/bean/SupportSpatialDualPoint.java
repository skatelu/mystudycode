package com.espertech.esper.supportregression.bean;

import java.io.Serializable;

public class SupportSpatialDualPoint implements Serializable {
    private String id;
    private double x1;
    private double y1;
    private double x2;
    private double y2;

    public SupportSpatialDualPoint(String id, double x1, double y1, double x2, double y2) {
        this.id = id;
        this.x1 = x1;
        this.y1 = y1;
        this.x2 = x2;
        this.y2 = y2;
    }

    public String getId() {
        return id;
    }

    public double getX1() {
        return x1;
    }

    public double getY1() {
        return y1;
    }

    public double getX2() {
        return x2;
    }

    public double getY2() {
        return y2;
    }
}
